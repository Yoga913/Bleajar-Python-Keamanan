# Di sini kita akan membuat sebuah tuple, yang merupakan urutan informasi yang teratur
tim_rj = ('Botafogo', 'Flamengo', 'Fluminense', 'Vasco')

# Kita akan mencetak jenis kelas
print(type(tim_rj)) # class='tuple'

# Kita akan mencetak informasi di layar
print(tim_rj) # ('Botafogo', 'Flamengo', 'Fluminense', 'Vasco')

tim_rj = ('Botafogo', 'Flamengo', 'Fluminense', 'Vasco')

